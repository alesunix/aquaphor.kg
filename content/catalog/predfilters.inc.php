<h3>Что такое предфильтры</h3>

Предфильтр, или фильтр предварительной очистки, устанавливается в магистраль на входе в домашнюю систему водоснабжения. Отсюда второе название предфильтров — магистральные фильтры. Чаще всего они очищают воду из городского водопровода или являются частью системы водоподготовки в коттедже.
<br><br>
Магистральные фильтры обеспечивают грубую механическую очистку воды от песка, ржавчины и ила. Предфильтры с угольным картриджем удаляют из воды еще и хлор, фенолы, бензолы, тяжёлые металлы и пестициды. После предварительной очистки вода становится более прозрачной и избавляется от посторонних запахов, однако остается непригодной для питья.
<br><br>

