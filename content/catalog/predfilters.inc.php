<h3>Как выбрать предфильтр</h3>
При выборе предфильтра ответьте на вопросы.
<br><br>
<b>Сколько воды в месяц нужно очищать?</b>
<br>
Посмотрите по показаниям счётчика, сколько в среднем литров воды в месяц вы тратите. Это влияет на размер корпуса, выбор картриджа и частоту его замены.
<br><br>
<b>Где установить предфильтр?</b>
<br>
Спланируйте пространство, где будете устанавливать фильтр, и определите подходящий размер. Как правило, для замены картриджа в предфильтре надо отсоединить нижнюю часть корпуса вместе с картриджем от верхней части. Оставляйте запас места под предфильтром.
Если место установки труднодоступно, обратите внимание на предфильтры с типом крепления «американка» — будет легче устанавливать.
<br><br>
<b>Нужно очистить холодную или горячую воду?</b>
<br>
Корпус и сменный модуль предфильтров должны выдерживать соответствующие температуры. Холодной считается вода с температурой от +5 до +35 градусов Цельсия, горячей — выше +35 градусов.
Кстати, при центральном горячем водоснабжении, горячая вода грязнее холодной.
<br><br>
<b>Какая степень очистки воды нужна?</b>
<br>
Для удаления песка, ржавчины, ила и механических примесей достаточно предфильтра с полипропиленовым картриджем. Угольный картридж глубже очистит воду — он удалит хлор, фенолы, бензолы, пестициды, тяжёлые металлы. Вода после глубокой очистки не сушит кожу и не вызывает аллергию. Однако обслуживание такого фильтра стоит дороже.
Мы посоветуем вам предфильтр, который устранит недостатки воды, поступающей в ваш водопровод.
