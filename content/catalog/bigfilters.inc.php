<h3>Фильтры под мойку</h3>

Фильтры для воды под мойку отличаются от фильтров-кувшинов тем, что:<br><br>
<font color=orange>&#10004;	</font>очищают воду быстрее;<br>
<font color=orange>&#10004;	</font>очищают воду лучше;<br>
<font color=orange>&#10004;	</font>вы меняете картриджи реже;<br>
<font color=orange>&#10004;	</font>питьевая вода свежая, потому что не застаивается.<br><br>

Фильтры под мойку очищают водопроводную воду от загрязнителей, которые встречаются в ней наиболее часто – от ржавчины, хлора и органических веществ (пестициды, фенолы, нефтепродукты), – делают воду безопасней, устраняют неприятный привкус, цвет и запах. Водоочистители производства Аквафор надежно защищают еще и от тяжелых металлов: свинца, ртути и других.
<br><br><h3>Типы фильтров с отдельным краном (фильтров под мойку):</h3>


<font color=orange>&#10004;	</font><b><big><font color=#4682B4>Фильтры обратного осмоса</big></font> — самая эффективная и популярная технология очистки воды в мире. Вода продавливается через мембрану, которая не пропускает вредные примеси. Они остаются на поверхности мембраны и затем смываются в канализацию. 
</b><br><br>
Как раз по этой технологии производят качественную бутилированную воду, и продают ее вам в магазине. Бытовой фильтр обратного осмоса — кухонный автомат, который делает воду премиум-класса прямо у вас дома. Причем из воды любого исходного качества, какие бы примеси в ней ни содержались (вам даже не нужно знать, какие). 
<br>
Фильтры обратного осмоса лучше, чем любые другие, подходят для жесткой воды: они полностью решают проблемы с накипью, делают воду мягкой и идеально чистой. Именно мягкая вода нужна для приготовления вкусного чая, кофе, супа и домашних заготовок. Кроме жесткости и уже перечисленных загрязнителей из воды удаляются: нитраты, антибиотики, гормоны, бактерии, вирусы и цисты (яйца микроорганизмов-паразитов, например, лямблий).
<br><br>
<font color=orange>&#10004;	</font><b><big><font color=#4682B4>Сорбционные фильтры.</big></font> Они впитывают и удерживают вредные примеси, как губка. Поток воды проходит насквозь, поэтому их еще называют проточными фильтрами. 
Обычно в таких фильтрах используется активированный кокосовый уголь. В фильтрах Аквафор уголь смешан с волокном Аквален, которое удаляет тяжелые металлы, ускоряет очистку воды и помогает картриджам дольше не забиваться.
<br><br></b>
Проточные фильтры больше подходят для мягкой воды: они не очень хорошо справляются с жесткостью. Ресурс специальных умягчающих картриджей ограничен, и чем более жесткая у вас вода, тем раньше в чайнике снова появится накипь. Умягчающие модули приходится часто менять или регенерировать солевым раствором. Но даже если в чайнике снова накипь, проточный фильтр по-прежнему защищает вас от вредных примесей (пока есть ресурс у остальных модулей).
