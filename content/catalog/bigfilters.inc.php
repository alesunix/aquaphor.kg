<h3>Как выбрать фильтр для воды под мойку?</h3>

Чтобы купить подходящий фильтр, следуйте трем главным советам:<br><br>
<font color=orange>&#10004;	</font><b>У вас жесткая вода — вам нужна система обратного осмоса. <br></b>

Верные признаки жесткой воды: накипь в чайнике, известковый налет на сантехнике, разводы и пятна на мытой посуде. Фильтры обратного осмоса полностью устраняют жесткость: улучшают вкус еды и напитков, защищают от накипи чайник, мультиварку и кофе-машину – техника служит дольше. 
<br><br>
Обратите внимание на две модели Аквафор: DWM-101 со встроенным баком для чистой воды на 5 литров, и DWM-31 – более компактный вариант, который наливает чистую воду сразу в кувшин.
<br><br>
<font color=orange>&#10004;	</font><b>Вы хотите пить воду без кипячения — выбирайте систему обратного осмоса или проточный фильтр для воды с половолоконной мембраной. 
<br></b>
Врачи-иммунологи советуют обеззараживать питьевую воду людям с чувствительным пищеварением или слабым иммунитетом, что характерно для маленьких детей, аллергиков и пожилых людей. Задача — удалить бактерии, вирусы и паразитов. С ними справится фильтр обратного осмоса или проточный фильтр с половолоконной мембраной. Например, Аквафор Кристалл ЭКО устраняет из воды бактерии, цисты и крупные вирусы без использования опасных для здоровья химических добавок. В нем установлен картридж с тончайшими пористыми трубками, с размером пор менее 0,1 микрона. Бактерии и цисты — крупнее, поэтому отсекаются механически. Похожая технология используется в медицине для очистки плазмы крови.
<br><br>
<font color=orange>&#10004;	</font><b>Для защиты от основных загрязнителей в водопроводной воде вам подойдет любой проточный фильтр. Однако у каждого семейства фильтров для питьевой воды есть свои преимущества. 
<br></b>
Среди проточных фильтров Аквафор самые удобные в использовании — фильтры семейства Кристалл. Чтобы заменить картридж в Кристалле, не нужно прикладывать физическую силу или искать специальные инструменты. К тому же отработанные модули снимаются вместе с корпусом, поэтому вы не соприкасаетесь с вредными загрязнителями. 
<br><br>
Если вы устанавливаете кран с фильтром для питьевой воды в офисе, детском саду, йога-клубе или в семье, где больше пяти человек — выбирайте фильтр, рассчитанный на повышенное потребление воды (с большим ресурсом сменных модулей). Подойдут, например, водоочистители Аквафор Фаворит: с их картриджем вы получаете 12 тысяч литров чистой воды. 
<br><br>
Какой бы фильтр вы ни выбрали для кухни, помните — важно вовремя менять картриджи. Любой фильтр становится бесполезен, если они исчерпали свой ресурс. Зарегистрируйте ваш фильтр Аквафор и мы напомним вам о замене.

